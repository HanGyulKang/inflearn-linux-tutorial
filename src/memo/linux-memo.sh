# mkdir [filename]
    디렉토리 생성

# rm [filename]
    파일 삭제
    # -r [directory]
        디렉토리 및 하위 파일 전체 삭제
    # rmdir
        디렉토리 삭제

# cd
    change directory

# ls
    list segments : 목록 표시
    # -l
        long format, list로 표시, permission 정보 표시
    # -a
        "." 포함 관련 디렉토리 모든 파일 표시
        # -al 로 합체 가능

# pwd
    현재 위치 표시